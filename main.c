Heading changeHeading(Heading h, int n)
{
	if (n > 0)
	{
		if (h == NORTH)
			return EAST;
		else if (h == SOUTH)
			return WEST;
		else if (h == EAST)
			return SOUTH;
		else if (h == WEST)
			return NORTH;
	}
	else if (n < 0)
	{
		if (h == NORTH)
			return WEST;
		else if (h == SOUTH)
			return EAST;
		else if (h == EAST)
			return NORTH;
		else if (h == WEST)
			return SOUTH;
	}
}

